
public class PlayerSideProject
{
    private String name;
    private int age;
    private String school;
    /**
     * Constructor for objects of class players
     */
    public  PlayerSideProject(String name, int age, String school)
    {
    this.name = name;
    this.age = age;
    this.school = school;
    }
    public String getName(){
     return name;
    }
    public String getSchool(){
     return school;
    }
    public int getAge(){
     return age;
    }
    
    
    
}
